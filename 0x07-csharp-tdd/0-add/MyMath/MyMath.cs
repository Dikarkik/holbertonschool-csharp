using System;

namespace MyMath
{
    /// <summary>
    /// This class defines math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Method that adds 2 numbers
        /// <param name="a">integer</param>
        /// <param name="b">integer</param>
        /// <returns>Sum of a and b</returns>
        /// </summary>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
